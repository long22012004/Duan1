    public void loadDataToTable() {
        model.setRowCount(0);
        ArrayList<chucvu> lst = cvsv.getAllchucvu();

        for (chucvu sa : lst) {
            model.addRow(new Object[]{sa.getId(),
                sa.getTencv(),});
        }

    }

    private void showDetail() {
        int index = tblcv.getSelectedRow();
        lbID.setText(tblcv.getValueAt(index, 0).toString());
        txtten.setText(tblcv.getValueAt(index, 1).toString());

    }

    public boolean validateform() {
        if (txtten.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return false;
        }
          ArrayList<chucvu> listsp = (ArrayList<chucvu>) cvsv.getAllchucvu();
        int listSize = listsp.size();
        for (int i = 0; i < listSize; i++) {
            if (listsp.get(i).getTencv().equalsIgnoreCase(txtten.getText())) {
                JOptionPane.showMessageDialog(this, "Ma nay da ton tai truoc do");
                return false;
            }
        }
        return true;
    }

