    private chucvu getChucVuFromInput() {
        chucvu nv = new chucvu();

        nv.setTencv(txtten.getText());

        return nv;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (validateform()) {
            chucvu nv = getChucVuFromInput();
            boolean add = cvsv.addchucvu(nv);
            if (add == true) {
                lst = cvsv.getAllchucvu();
                loadDataToTable();
                JOptionPane.showMessageDialog(this, "Them thanh cong");
            } else {
                JOptionPane.showMessageDialog(this, "Them that bai");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = tblcv.getSelectedRow();
        int colum = 0;
        if(row ==-1){
            JOptionPane.showMessageDialog(this, "Ban chua chon chuc vu can sua");
        }
        if (row >= 0) {
            int ma = (int) tblcv.getValueAt(row, colum);
            if (validateform()) {
                chucvu d = getChucVuFromInput();
                if (d != null) {
                    boolean update = cvsv.updatenhanvien(d, ma);
                    if (update == true) {
                        lst = cvsv.getAllchucvu();
                        loadDataToTable();
                        JOptionPane.showMessageDialog(this, "cap nhat thanh cong");
                    } else {
                        JOptionPane.showMessageDialog(this, "Cap nhat that bai");
                        return;
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

